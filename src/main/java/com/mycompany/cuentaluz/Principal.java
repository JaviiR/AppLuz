    private void CambiosInciales() {

        txtPrincipal.setBorder(new BottomBorder(1, new Color(55, 55, 56, ABORT)));
        txtSecundario.setBorder(new BottomBorder(1, new Color(55, 55, 56, ABORT)));
        BordeRedondeado btnCalcular = new BordeRedondeado("CALCULAR", 35);
        btnCalcular.setBounds(15, 140, 270, 40);
        btnCalcular.setOpaque(true);
        btnCalcular.setBackground(new Color(26, 66, 99));
        btnCalcular.setForeground(Color.white);
        btnCalcular.setHorizontalAlignment(SwingConstants.CENTER);
        Font nuevaFuente = new Font("Franklin Gothic Demi", Font.ITALIC, 25);
        btnCalcular.setFont(nuevaFuente);
        btnCalcular.setCursor(new Cursor(Cursor.HAND_CURSOR));
        jPanel1.add(btnCalcular);
        btnCalcular.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                btnCalcular.setBackground(Color.BLACK);

            }

            @Override
            public void mouseExited(MouseEvent e) {
                btnCalcular.setBackground(new Color(26, 66, 99));
            }

            @Override
            public void mouseClicked(MouseEvent e) {
                if (e.getClickCount() == 1) {
                    String principal = txtPrincipal.getText();
                    String secundario = txtSecundario.getText();
                    if (principal.length() == 4 && secundario.length() == 5) {
                        if (calcular()) {
                            txtTotal.setText("TOTAL: " + total + "0");
                        } else {
                            txtTotal.setText("TOTAL: " + total + "0");
                            int i = JOptionPane.showConfirmDialog(null,
                                    "ya existe un registro de este mes.\nDesea ver los registros?", "ERROR!", 0,
                                    JOptionPane.OK_CANCEL_OPTION, null);
                            if (i == 0) {
                                new ListaDatos().setVisible(true);
                            } else {
                                System.out.println("no quiso abrir los registros");
                            }
                        }
                    }else{
                        JOptionPane.showMessageDialog(null, "validar los siguientes campos:\n-En el campo principal son 4 digitos\n-En el campo secundario son 5 digitos", "ERROR!", JOptionPane.ERROR_MESSAGE);
                    }

                }
            }

        });

        con = new Conexion();
        lblLogo.setIcon(CrearImg(new ImageIcon(Principal.class.getResource("/imgs/logoLuz.png")), lblLogo));
        lblFecha.setEnabled(false);
        txtTotal.setEnabled(false);
        LocalDate fechaActual = LocalDate.now();
        String fecha = fechaActual.format(DateTimeFormatter.ofPattern("dd-MM-yyyy"));
        lblFecha.setText(fecha);
        MedAnterior = con.MedicionAnterior(lblFecha.getText());

    }

    private boolean calcular() {

        int MedActualPrincipal = Integer.parseInt(txtPrincipal.getText());
        System.err.println("Principal: " + MedActualPrincipal);
        double MedActualSecundario = Double.parseDouble(txtSecundario.getText());
        System.err.println("Secundario: " + MedActualSecundario);
        double DifMediciones = (MedActualSecundario - MedAnterior) / 10;
        System.err.println("Diferencia mediciones Jose: " + DifMediciones);
        double CargPrincipal = (int) (MedActualPrincipal + (0.31 * MedActualPrincipal));
        System.err.println("principal+31%: " + CargPrincipal);
        double Total = (CargPrincipal * DifMediciones) / 10000;
        BigDecimal TotalRedondeado = new BigDecimal(Total);
        TotalRedondeado = TotalRedondeado.setScale(1, RoundingMode.HALF_UP);
        System.err.println("TOTAL: " + Total);
        boolean validar = con.IngresarDatos(lblFecha.getText(), (int) MedActualPrincipal, (int) MedActualSecundario,
                TotalRedondeado.doubleValue());
        if (validar) {
            total = TotalRedondeado.doubleValue();
        }
        return validar;
    }

